//        ??when nextButton clicked
            
        
            try{
                isExist = DatabaseManager.c.isCustomerExist(AccNo);
                
                ApplicationManager.incrementAttempt();
                
                if(isExist)
                {
                  ApplicationManager.setAttempt(0);
                  ApplicationManager.application.loadScene(new KeyValidation()); 
                  
                }
                else if(attempt < 3){

                    ApplicationManager.application.loadScene(new AccountValidation());
                    ApplicationManager.Notify.sendNotification("Invalid Account no",1);
                }
                else{
                    
                    ApplicationManager.application.loadScene(new Welcome());
                    
                    
                }
                
                
            }catch(Exception e)
            {
                System.out.println(e);
            }
        
        
        
    
        
        
        
        System.out.println("Number of attempts:  " + attempt);
        
