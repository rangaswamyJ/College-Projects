    private void AccountNoKeyReleased(KeyEvent evt) {//GEN-FIRST:event_AccountNoKeyReleased
        // TODO add your handling code here:
        
         JTextField an = (JTextField)evt.getSource();
        

        
        try{
            if(AccNoLen != 1)
            {
                AccNo = Long.parseLong(an.getText());
            }
        }catch(Exception e)
        {
            System.out.println("Account Number does not exist");
        }
        
        AccNoLen =  an.getText().length() ;
        
        if(AccNoLen == 9)
        {
           // System.out.println("Invalid account no");
            an.setEnabled(false);
            nextButton.setEnabled(true);
            DatabaseManager.cust.AccountNo = AccNo;
            
        }
    }//GEN-LAST:event_AccountNoKeyReleased

